# Дано послідовність цілих чисел, яка вводиться в рядку через пропуск. Визначте два найменших елемента послідовності.
# Вони можуть бути як рівні між собою (обидва бути мінімальними), так і відрізнятися.
#
# Вхідні дані:
#
# 14 3 40 56 42 43 89 69 64 72 5 44 11 25
#
# Вихідні дані:
#
# 5 3

numbers = list(map(int, input(f"Enter numbers: ").split()))
min1 = min(numbers)
numbers.pop(numbers.index(min1))
min2 = min(numbers)
print(min2, min1)



