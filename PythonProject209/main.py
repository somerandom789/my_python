# Напишіть програму, яка приймає послідовність 4-цифрових двійкових чисел,
# розділених комами, і друкує числа, які ділиться на 5 без остачі, в рядку і розділені комами.
#
# Вхідні дані:
#
# 0100,0011,1010,1001,1100
#
# Вихідні дані:
#
# 1010

numbers = input("Enter numbers: ").split(",")
for it in numbers:
    decimal_number = int(it, 2)
    if decimal_number % 5 == 0:
        print(it)


