# Знайти відстань між двома точками із заданими координатами (x1, y1, x2, y2)
# в 2-вимірному просторі. Значення координат є цілими числами. Результат округлити
# до двох знаків після десяткової крапки.
#
# Вхідні дані:
#
# 2 3 -7 4
#
# Вихідні дані:
#
# 9.06

x1, y1, x2, y2 = map(int, input(f"Enter numbers: ").split())
distance = ((x2 - x1)**2 + (y2 - y1)**2) ** 0.5
print(f"{distance:.2f}")